async function main() {
  let n = 0;
  for (const name of CITIES) {
    const sortOrder = name === 'Москва' ? 1 : name === 'Санкт-Петербург' ? 2 : 100;
    await prisma.city.upsert({
      where: { name },
      update: { sortOrder, country: 'Россия' },
      create: { name, country: 'Россия', sortOrder },
    });
    n++;
  }
  console.log(`Seeded ${n} cities`);
}

